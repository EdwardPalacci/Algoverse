def main() -> None:
    for directory in [DATA_DIR, OUTPUTS_DIR, METRICS_DIR, FIG_DIR, DOCS_DIR]:
        directory.mkdir(parents=True, exist_ok=True)
    rows, raw_counts = load_all_rows()
    common_qids = shared_question_ids(rows)
    aligned_rows = [r for r in rows if r["question_id"] in common_qids]
    produce_alignment_report(rows, aligned_rows)
    produce_tables(aligned_rows, raw_counts)
    produce_figures(aligned_rows)
    produce_table_captions()
    produce_audit_and_cases(aligned_rows)
    produce_docs(rows, raw_counts)
    produce_schema_files()
    produce_manifest()
    print(f"generated review artifacts from {len(aligned_rows)} aligned parsed generations")
    print(f"shared question_id values: {len(common_qids)}")
    print("review materials written to docs/, fig_tabs/, metrics/, and src/")


