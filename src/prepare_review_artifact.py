"""Generate review-ready benchmark artifacts from saved pilot outputs.

This script intentionally uses only the Python standard library so it can run
in the lightweight review environment currently present in this repository.
Correctness labels are generated by a deterministic automatic grader; the
manual audit files reserve columns for later adjudication.
"""

