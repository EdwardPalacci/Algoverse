HIGH_CONFIDENCE_THRESHOLD = 0.90
ECE_BINS = 10


def read_jsonl(path: Path) -> list[dict]:
    rows = []
    with path.open() as f:
        for line in f:
            line = line.strip()
            if line:
                rows.append(json.loads(line))
    return rows


def write_csv(path: Path, rows: list[dict], fieldnames: list[str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        for row in rows:
            writer.writerow({k: row.get(k, "") for k in fieldnames})


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def normalized_text(value: object) -> str:
    if value is None:
        return ""
    text = unicodedata.normalize("NFKD", str(value)).encode("ascii", "ignore").decode("ascii")
    text = text.lower()
    text = re.sub(r"[^a-z0-9]+", " ", text)
    return " ".join(text.split())


def numeric_tokens(value: object) -> list[Decimal]:
    tokens = re.findall(r"[-+]?\d[\d,]*(?:\.\d+)?", str(value or ""))
    out = []
    for token in tokens:
        try:
            out.append(Decimal(token.replace(",", "")))
        except InvalidOperation:
            pass
    return out


def choice_map(prompt: str) -> dict[str, str]:
    out = {}
    for line in prompt.splitlines():
        m = re.match(r"^\s*([A-D])\s*[:.)]\s*(.+?)\s*$", line)
        if m:
            out[m.group(1)] = m.group(2)
    return out


def answer_letter(answer: object) -> str | None:
    text = str(answer or "").strip()
    patterns = [
        r"^\s*([A-D])\s*[:.)\s]",
        r"\boption\s+([A-D])\b",
        r"\banswer\s+is\s+([A-D])\b",
    ]
    for pattern in patterns:
        m = re.search(pattern, text, flags=re.IGNORECASE)
        if m:
            return m.group(1).upper()
    if re.fullmatch(r"[A-D]", text, flags=re.IGNORECASE):
        return text.upper()
    return None


def truth_values(ground_truth: object) -> list[str]:
    if isinstance(ground_truth, list):
        return [str(x) for x in ground_truth]
    if ground_truth is None:
        return []
    return [str(ground_truth)]


def auto_grade(row: dict) -> tuple[bool, str]:
    answer = row.get("answer")
    truths = truth_values(row.get("ground_truth"))
    answer_type = row.get("answer_type")
    prompt = row.get("prompt", "")

    if not answer or not truths:
        return False, "missing_answer_or_reference"

    if answer_type == "numeric":
        answer_nums = numeric_tokens(answer)
        truth_nums = [n for truth in truths for n in numeric_tokens(truth)]
        if answer_nums and truth_nums and answer_nums[-1] == truth_nums[-1]:
            return True, "numeric_last_value_match"
        return False, "numeric_last_value_mismatch"

    options = choice_map(prompt)
    letter = answer_letter(answer)
    if answer_type == "multiple_choice" and letter:
        normalized_truths = {normalized_text(t) for t in truths}
        if letter in normalized_truths:
            return True, "choice_letter_match"
        if letter in options and normalized_text(options[letter]) in normalized_truths:
            return True, "choice_option_text_match"

    norm_answer = normalized_text(answer)
    for truth in truths:
        norm_truth = normalized_text(truth)
        if norm_answer == norm_truth:
            return True, "string_exact_match"
        if norm_truth and (norm_truth in norm_answer or norm_answer in norm_truth):
            return True, "string_containment_match"

    return False, "string_mismatch"


def load_all_rows() -> tuple[list[dict], dict[str, int]]:
    sources = [
        ("outputs/ar_parsed_generations.jsonl", AR_PARSED, AR_RAW),
        ("dlm_outputs/dlm_parsed_generations.jsonl", DLM_PARSED, DLM_RAW),
    ]
    rows = []
    raw_counts = {}
    for source_name, parsed_path, raw_path in sources:
        parsed = read_jsonl(parsed_path)
        raw_counts[source_name] = sum(1 for line in raw_path.open() if line.strip())
        for row in parsed:
            row = dict(row)
            row["source_file"] = source_name
            row["model_id"] = row.get("model_name", "")
            row["model_family"] = row.get("model_architecture", "")
            row["prompt_condition"] = row.get("condition", "")
            row["parsed_confidence"] = float(row.get("confidence")) if row.get("confidence") is not None else None
            row["correct_auto"], row["grader_rule"] = auto_grade(row)
            rows.append(row)
    return rows, raw_counts


def mean(values: Iterable[float]) -> float | None:
    values = list(values)
    return sum(values) / len(values) if values else None


def ece(rows: list[dict], bins: int = ECE_BINS) -> float | None:
    usable = [r for r in rows if r.get("parsed_confidence") is not None and r.get("correct_auto") is not None]
    if not usable:
        return None
    total = len(usable)
    score = 0.0
    for b in range(bins):
        lo = b / bins
        hi = (b + 1) / bins
        if b == bins - 1:
            bucket = [r for r in usable if lo <= r["parsed_confidence"] <= hi]
        else:
            bucket = [r for r in usable if lo <= r["parsed_confidence"] < hi]
        if not bucket:
            continue
        acc = mean(1.0 if r["correct_auto"] else 0.0 for r in bucket)
        conf = mean(r["parsed_confidence"] for r in bucket)
        score += (len(bucket) / total) * abs(acc - conf)
    return score


def brier(rows: list[dict]) -> float | None:
    vals = []
    for r in rows:
        c = r.get("parsed_confidence")
        if c is not None and r.get("correct_auto") is not None:
            vals.append((c - (1.0 if r["correct_auto"] else 0.0)) ** 2)
    return mean(vals)


def auroc(rows: list[dict]) -> float | None:
    pos = [r["parsed_confidence"] for r in rows if r.get("parsed_confidence") is not None and r.get("correct_auto") is True]
    neg = [r["parsed_confidence"] for r in rows if r.get("parsed_confidence") is not None and r.get("correct_auto") is False]
    if not pos or not neg:
        return None
    wins = 0.0
    for p in pos:
        for n in neg:
            if p > n:
                wins += 1.0
            elif p == n:
                wins += 0.5
    return wins / (len(pos) * len(neg))


def metric_row(rows: list[dict], raw_count: int | None = None) -> dict:
    n = len(rows)
    correct = sum(1 for r in rows if r.get("correct_auto") is True)
    confs = [r["parsed_confidence"] for r in rows if r.get("parsed_confidence") is not None]
    hc_wrong = sum(
        1
        for r in rows
        if r.get("correct_auto") is False
        and r.get("parsed_confidence") is not None
        and r["parsed_confidence"] >= HIGH_CONFIDENCE_THRESHOLD
    )
    return {
        "N": n,
        "accuracy": correct / n if n else None,
        "mean_confidence": mean(confs),
        "expected_calibration_error": ece(rows),
        "brier_score": brier(rows),
        "area_under_roc": auroc(rows),
        "high_confidence_wrong_rate": hc_wrong / n if n else None,
        "parse_success": n / raw_count if raw_count else None,
    }


def fmt(value: object) -> object:
    if value is None:
        return "NA"
    if isinstance(value, float):
        return f"{value:.6f}"
    return value


def grouped(rows: list[dict], keys: tuple[str, ...]) -> dict[tuple, list[dict]]:
    out = defaultdict(list)
    for row in rows:
        out[tuple(row.get(k, "") for k in keys)].append(row)
    return dict(out)


def shared_question_ids(rows: list[dict]) -> set[str]:
    qsets = {
        family[0]: {r["question_id"] for r in rs}
        for family, rs in grouped(rows, ("model_family",)).items()
    }
    if {"AR", "DLM"} <= set(qsets):
        return qsets["AR"] & qsets["DLM"]
    return set().union(*qsets.values()) if qsets else set()


def produce_alignment_report(rows: list[dict], aligned_rows: list[dict]) -> None:
    report = []
    by_family = grouped(rows, ("model_family",))
    qsets = {family[0]: {r["question_id"] for r in rs} for family, rs in by_family.items()}
    if {"AR", "DLM"} <= set(qsets):
        ar_only = sorted(qsets["AR"] - qsets["DLM"])
        dlm_only = sorted(qsets["DLM"] - qsets["AR"])
        status = "pass" if not ar_only and not dlm_only else "fail"
        report.append({
            "check_name": "same_question_id_set_raw_sources",
            "status": status,
            "n_affected": len(ar_only) + len(dlm_only),
            "details": f"AR-only={len(ar_only)}; DLM-only={len(dlm_only)}",
        })
        write_csv(
            METRICS_DIR / "data_alignment_exclusions.csv",
            [{"question_id": qid, "excluded_from": "comparative_tables", "reason": "AR-only question_id"} for qid in ar_only]
            + [{"question_id": qid, "excluded_from": "comparative_tables", "reason": "DLM-only question_id"} for qid in dlm_only],
            ["question_id", "excluded_from", "reason"],
        )

    aligned_qsets = {
        family[0]: {r["question_id"] for r in rs}
        for family, rs in grouped(aligned_rows, ("model_family",)).items()
    }
    aligned_pass = len({frozenset(v) for v in aligned_qsets.values()}) <= 1
    report.append({
        "check_name": "same_question_id_set_comparative_analysis",
        "status": "pass" if aligned_pass else "fail",
        "n_affected": 0 if aligned_pass else 1,
        "details": f"{len(shared_question_ids(rows))} shared question_id values used for comparative tables and figures",
    })

    for label, keys in [
        ("duplicate_question_id_within_source", ("source_file", "question_id", "prompt_condition", "sample_id")),
    ]:
        counts = Counter(tuple(r.get(k, "") for k in keys) for r in rows)
        n = sum(v - 1 for v in counts.values() if v > 1)
        report.append({"check_name": label, "status": "pass" if n == 0 else "fail", "n_affected": n, "details": "duplicate full evaluation keys"})

    valid_datasets = {r["dataset"] for r in json.loads(PILOT_DATA.read_text())}
    valid_models = {"qwen/qwen-2.5-7b-instruct", "mercury-2"}
    valid_families = {"AR", "DLM"}
    valid_conditions = {"neutral", "cautious", "overconfident"}
    checks = [
        ("valid_dataset", lambda r: r.get("dataset") in valid_datasets),
        ("valid_model_id", lambda r: r.get("model_id") in valid_models),
        ("valid_model_family", lambda r: r.get("model_family") in valid_families),
        ("valid_prompt_condition", lambda r: r.get("prompt_condition") in valid_conditions),
        ("confidence_normalized_0_1", lambda r: isinstance(r.get("parsed_confidence"), float) and 0.0 <= r["parsed_confidence"] <= 1.0),
        ("correctness_binary_auto_label", lambda r: isinstance(r.get("correct_auto"), bool)),
    ]
    for name, predicate in checks:
        bad = [r for r in rows if not predicate(r)]
        report.append({"check_name": name, "status": "pass" if not bad else "fail", "n_affected": len(bad), "details": "" if not bad else "invalid rows present"})

    write_csv(METRICS_DIR / "data_alignment_report.csv", report, ["check_name", "status", "n_affected", "details"])


def produce_tables(rows: list[dict], raw_counts: dict[str, int]) -> None:
    table1 = []
    for (dataset, model, family, condition), rs in sorted(grouped(rows, ("dataset", "model_id", "model_family", "prompt_condition")).items()):
        table1.append({
            "dataset": dataset,
            "N": len(rs),
            "model": model,
            "family": family,
            "prompt_condition": condition,
            "confidence_scale": "verbalized probability in [0, 1]",
            "correctness_grader": "deterministic automatic string/numeric/choice grader",
            "metrics": "accuracy; mean confidence; expected calibration error; Brier score; area under the receiver operating characteristic curve; high-confidence wrong rate; parse success",
        })
    write_csv(FIG_DIR / "table_1_benchmark_specification.csv", table1, ["dataset", "N", "model", "family", "prompt_condition", "confidence_scale", "correctness_grader", "metrics"])

    table2 = []
    for (model, family, source_file), rs in sorted(grouped(rows, ("model_id", "model_family", "source_file")).items()):
        m = metric_row(rs, raw_counts.get(source_file))
        table2.append({"model": model, "family": family, **{k: fmt(v) for k, v in m.items()}})
    write_csv(FIG_DIR / "table_2_aggregate_metrics.csv", table2, ["model", "family", "N", "accuracy", "mean_confidence", "expected_calibration_error", "brier_score", "area_under_roc", "high_confidence_wrong_rate", "parse_success"])

    table3 = []
    for (dataset, model, family), rs in sorted(grouped(rows, ("dataset", "model_id", "model_family")).items()):
        m = metric_row(rs)
        table3.append({"dataset": dataset, "model": model, "family": family, **{k: fmt(v) for k, v in m.items() if k != "parse_success"}})
    write_csv(FIG_DIR / "table_3_per_dataset_metrics.csv", table3, ["dataset", "model", "family", "N", "accuracy", "mean_confidence", "expected_calibration_error", "brier_score", "area_under_roc", "high_confidence_wrong_rate"])

    table4 = []
    for (model, family, condition), rs in sorted(grouped(rows, ("model_id", "model_family", "prompt_condition")).items()):
        m = metric_row(rs)
        table4.append({"model": model, "family": family, "prompt_condition": condition, **{k: fmt(v) for k, v in m.items() if k not in {"area_under_roc", "parse_success"}}})
    write_csv(FIG_DIR / "table_4_prompt_condition_metrics.csv", table4, ["model", "family", "prompt_condition", "N", "accuracy", "mean_confidence", "expected_calibration_error", "brier_score", "high_confidence_wrong_rate"])


def reliability_points(rows: list[dict], family: str) -> list[tuple[float, float]]:
    rs = [r for r in rows if r["model_family"] == family]
    pts = []
    for b in range(ECE_BINS):
        lo, hi = b / ECE_BINS, (b + 1) / ECE_BINS
        if b == ECE_BINS - 1:
            bucket = [r for r in rs if lo <= r["parsed_confidence"] <= hi]
        else:
            bucket = [r for r in rs if lo <= r["parsed_confidence"] < hi]
        if bucket:
            pts.append((mean(r["parsed_confidence"] for r in bucket), mean(1.0 if r["correct_auto"] else 0.0 for r in bucket)))
    return pts


